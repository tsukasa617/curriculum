public class Sample3Upd {
    public static void main(String[] args) {

        int num3, num4;
        num3 = 5;
        System.out.println("変数num3の値は" + num3 + "です。");

        num4 = num3;
        System.out.println("変数num3の値を変数num4に代入しました。");
        System.out.println("変数num4の値は" + num4 + "です。");
        
    }
}
