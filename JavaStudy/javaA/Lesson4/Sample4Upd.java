public class Sample4Upd {

    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 55;

        System.out.println("num1とnum2にはいろいろな演算を行います。");
        System.out.println("num1+num2は" + (num1 + num2) + "です。");
        System.out.println("num1-num2は" + (num1 - num2) + "です。");
        System.out.println("num1*num2は" + (num1 * num2) + "です。");
        System.out.println("num1/num2は" + (num1 / num2) + "です。");
        System.out.println("num1%num2は" + (num1 % num2) + "です。");
        
    }
    
}
