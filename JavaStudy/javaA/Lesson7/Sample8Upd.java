public class Sample8Upd {

    public static void main(String[] args) {

        int [] test = { 80, 60, 22, 100, 95, 50 };

        for (int i = 0; i < test.length; i++) {
            System.out.println((i + 1) + "番目の人の点数は" + test [i] + "です。");
        }

        System.out.println("テストの受験者は" + test.length + "人です。");
        
    }

}
