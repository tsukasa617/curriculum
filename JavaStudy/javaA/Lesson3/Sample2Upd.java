public class Sample2Upd {
    public static void main(String[] args) {

        int num;
        num = 10;
        System.out.println("変数numの値は" + num + "です。");

        num = 15;
        System.out.println("変数numの値を変更いたしました。");
        System.out.println("変数numの新しい値は" + num + "です。");
        
    }
}
